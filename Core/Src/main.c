void set_servo_angle(TIM_HandleTypeDef *htim, uint32_t channel, int angle)
{
    angle = angle + 90;

    // 각도에 따른 PWM 듀티 사이클 계산 (0도 = 1ms, 180도 = 2ms)
    uint32_t pulse_length = (angle * (2000 - 1000) / 180 + 1000);
    __HAL_TIM_SET_COMPARE(htim, channel, pulse_length);
}



void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
    if (huart->Instance == USART1) {
        if (receivedData[data_index] == '\n') {
            receivedData[data_index++] = '\0'; // null terminate the string
            data_received = 1;
            data_index = 0;

        } else {

            data_index++;
        }

        HAL_UART_Receive_IT(&huart1, (uint8_t*)(receivedData + data_index), 1); // Receive next character
    }
}
